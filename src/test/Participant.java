package kidsactivitymanager;

public class Participant {

	private String name;
	private int age;

	public Participant() {
	}

	public Participant(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return this.age;
	}

	public void setAge(int age) {
		this.age = age;
	}



}